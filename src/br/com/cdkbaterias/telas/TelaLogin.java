    //e servem apra preparar e executar instruçoes sql
    PreparedStatement pst = null;
    ResultSet rs = null;

    public void logar() {
        String sql = "select * from usuarios where login=? and senha=?";
        try {
            //as linhas abaixo preparam a consulta ao banco em função do
            // que foi digitado nas caixas de texto. O ? é subistituido pelo
            //conteudo das variaves
            pst = conexao.prepareStatement(sql);
            pst.setString(1, c_usuario.getText());
            pst.setString(2, c_senha.getText());

            // a linha a baixo executa a query
            rs = pst.executeQuery();

            // se existir usuário e senha correspondente
            if (rs.next()) {
                // a linha abaixo obtem o conteudo do campo perfil da tabela tbusuario
                String perfil = rs.getString(5);
                // a estrutura abaixo faz o tratamento do perfil do usuário

                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.menuCad.setEnabled(true);
                    
                    // a linha abaixo traz uma informaçao do banco de dados e
                    // seta ela em uma label no caso lblusuario
                    
                    // a linha abaixo seta uma cor a lbl escolhida
                    
                    this.dispose();
                }else{
                TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    
                    TelaPrincipal.menuCad.setEnabled(true);
                    // a linha abaixo traz uma informaçao do banco de dados e
                    // seta ela em uma label no caso lblusuario
                    
                    this.dispose();
                
                }
                conexao.close();
            } else {
                JOptionPane.showMessageDialog(null, "Usuário e/ou senha inválido(s)");
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }

    }

